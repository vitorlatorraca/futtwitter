export function generateMorePosts(offset: number): Post[] {
  return mockPosts.map((post, i) => ({
    ...post,
    id: `${post.id}-extra-${offset}-${i}`,
    timestamp: subDays(post.timestamp, offset * 3 + i),
    likes: Math.floor(post.likes * (0.5 + Math.random())),
    views: Math.floor(post.views * (0.5 + Math.random())),
    reposts: Math.floor(post.reposts * (0.5 + Math.random())),
    replies: Math.floor(post.replies * (0.5 + Math.random())),
    liked: false,
    reposted: false,
    bookmarked: false,
  })).slice(0, 6);
}
